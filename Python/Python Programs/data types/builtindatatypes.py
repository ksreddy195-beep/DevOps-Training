# String
x = "Hello World"
print(x)

# Integer
x = 20
print(x)

# Float
x = 20.5
print(x)

# Complex
x = 1j
print("complex" , x)

# List
x = ["apple", "banana", "cherry"]
print(x)

# Tuple
x = ("apple", "banana", "cherry")
print(x)

# Range
x = range(6)
print(list(x))

# Dictionary
x = {"name": "John", "age": 36}
print(x)

# Set
x = {"apple", "banana", "cherry"}
print(x)

# Frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)

# Boolean
x = True
print(x)

# Bytes
x = b"Hello"
print(x)

# Bytearray
x = bytearray(5)
print(x)

# Memoryview
x = memoryview(bytes(5))
print(x)

# NoneType
x = None
print(x)